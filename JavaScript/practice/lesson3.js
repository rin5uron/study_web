// JavaScript超基礎 練習問題3

// 1. りんご計算関数 - りんごの個数を受け取り、値段を表示する関数
function りんご計算(個数) {
  return "りんごの値段は" + (個数 * 100) + "円です";
}

// 2. おやつ選び関数 - 食べ物の名前を受け取り、おやつのメッセージを返す関数
function おやつ選び(食べ物) {
  return `今日はおやつに${食べ物}を食べます`;
}

// 3. 自己紹介関数 - 名前と年齢を受け取り、自己紹介文を返す関数
function 自己紹介(名前, 年齢) {
  return `私の名前は${名前}です。${年齢}歳です。`;
}

// 4. 大きい数字関数 - 2つの数字を比較して大きい方を返す関数
function 大きい方(a, b) {
  if (a > b) {
    return `${a}の方が大きいです`;
  } else if (a < b) {
    return `${b}の方が大きいです`;
  } else {
    return "同じ大きさです";
  }
}

// 5. 好きな食べ物関数 - 食べ物のリストから好きなものを選ぶ関数
function 好きな食べ物(食べ物リスト) {
  let 選んだ食べ物 = 食べ物リスト[1]; // 2番目の食べ物を選ぶ
  return `${選んだ食べ物}が好きです`;
}

// 関数を呼び出してみよう
console.log(りんご計算(5));
console.log(おやつ選び("アイス"));
console.log(自己紹介("たろう", 10));
console.log(大きい方(10, 5));

let 食べ物 = ["りんご", "バナナ", "オレンジ"];
console.log(好きな食べ物(食べ物)); 